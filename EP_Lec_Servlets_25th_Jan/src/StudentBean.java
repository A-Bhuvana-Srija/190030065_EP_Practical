public class StudentBean
{
	//members
	private String name;
	private int id;
	
	//constructor
	public StudentBean()
	{
		this.id = id;
		this.name = name;
	}
	
	//getters and setters
	//for name
	public void setName(String name)
	{
		this.name = name;
	}
	public String getName()
	{
		return name;
	}
	
	//for id
	public void setId(int id)
	{
		this.id = id;
	}
	public int getId()
	{
		return id;
	}
}